  PRIMARY KEY (`position_id`)
  ) ENGINE=InnoDB DEFAULT CHARSET=latin1;


CREATE TABLE `team` (
  `team_id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `code_name` varchar(255) NOT NULL,
